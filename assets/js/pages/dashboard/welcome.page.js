parasails.registerPage('welcome', {
  //  ╦╔╗╔╦╔╦╗╦╔═╗╦    ╔═╗╔╦╗╔═╗╔╦╗╔═╗
  //  ║║║║║ ║ ║╠═╣║    ╚═╗ ║ ╠═╣ ║ ║╣
  //  ╩╝╚╝╩ ╩ ╩╩ ╩╩═╝  ╚═╝ ╩ ╩ ╩ ╩ ╚═╝
  data: {
    dashboardModalVisible: false,
  },

  //  ╦  ╦╔═╗╔═╗╔═╗╦ ╦╔═╗╦  ╔═╗
  //  ║  ║╠╣ ║╣ ║  ╚╦╝║  ║  ║╣
  //  ╩═╝╩╚  ╚═╝╚═╝ ╩ ╚═╝╩═╝╚═╝
  beforeMount: function () {
    // Attach any initial data from the server.
    _.extend(this, SAILS_LOCALS);
  },
  mounted: async function () {
    // trả về thông tin sơ khởi của toàn bộ hệ thống
    function getBoardLayout(callback) {
      $.getJSON("/board", function (result) {
        callback(result);
      });
    };


    getBoardLayout(function (globalBoardData) {
      let sumBoard = globalBoardData.length;
      let active = 0, deactive = 0;
      for (let i = 0; i < sumBoard; i++) {
        globalBoardData[i].status == "active" ? active++ : deactive++;
      }
      $("#sumBoard").text(sumBoard);
      $("#active").text(active);
      $("#deactive").text(deactive);
    });
  },

  //  ╦╔╗╔╔╦╗╔═╗╦═╗╔═╗╔═╗╔╦╗╦╔═╗╔╗╔╔═╗
  //  ║║║║ ║ ║╣ ╠╦╝╠═╣║   ║ ║║ ║║║║╚═╗
  //  ╩╝╚╝ ╩ ╚═╝╩╚═╩ ╩╚═╝ ╩ ╩╚═╝╝╚╝╚═╝
  methods: {

    clickOpenDashboardModalButton: async function () {
      this.dashboardModalVisible = true;
    },

    closeDashboardModal: async function () {
      this.dashboardModalVisible = false;
    },

  }
});
